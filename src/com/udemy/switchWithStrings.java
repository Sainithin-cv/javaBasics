package com.udemy;

import java.util.Locale;

public class switchWithStrings {
    public static void main(String... args){
        String day= "moNday";
        switch (day.toLowerCase(Locale.ROOT)){
            case "monday":
                System.out.println("monday");
                break;
            case "tuesday":
                System.out.println("monday");
                break;
            case "wednesday":
                System.out.println("monday");
                break;
            case "thursday":
                System.out.println("monday");
                break;
            case "friday":
                System.out.println("monday");
                break;
            case "saturday":
                System.out.println("monday");
                break;
            case "sunday":
                System.out.println("monday");
                break;

        }
    }
}
